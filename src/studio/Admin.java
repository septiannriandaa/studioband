    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel tabeladmin = new DefaultTableModel();
        tabeladmin.addColumn("user");
        tabeladmin.addColumn("password");
        tabeladmin.addColumn("role");
        role.setModel(tabeladmin);
        try{
            Statement st = (Statement) Koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("select * from admin");
            while(rs.next())
            {
                tabeladmin.addRow(new Object[]{
                    rs.getString("User"),
                    rs.getString("Password"),
                    rs.getString("Role"),
                });
                role.setModel(tabeladmin);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Maaf Data Kosong");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

