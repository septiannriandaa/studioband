    private void roleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_roleMouseClicked
        int i = role.getSelectedRow();
        if(i>=0){
            user.setText(role.getValueAt(i,0).toString());
            pass.setText(role.getValueAt(i,1).toString());
            comboRole.setSelectedItem(role.getValueAt(i,2));
        }
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);
    }//GEN-LAST:event_roleMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nama = user.getText();
        String pas = pass.getText();

        String role;
        role = (String) comboRole.getSelectedItem();

        if(nama.isEmpty() || pas.isEmpty() || role.isEmpty()){
            JOptionPane.showMessageDialog(null, "Tolong Lengkapi Data Anda!");
        }else{
            try{
                Statement st = (Statement) Koneksi.getConnection().createStatement();
                st.executeUpdate("insert into admin (user,password,role) values ('"+nama+"','"+pas+"','"+role+"');");
                JOptionPane.showMessageDialog(null,"Input data berhasil...");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Gagal input data");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

